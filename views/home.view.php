                <div class="tab-pane" role="tabpanel" id="tab-2">
                    <h3>Distinguishing Your Business: </h3>
                    <p>Trademarks make it easier for customers to find and recognize your business in a crowded marketplace1</p>
                    <h3>Valuable Asset: </h3>
                    <p>As your business grows, so does the value of your trademark. It can be bought, sold, licensed, or used as a security interest to secure a loan</p>
                    <h3>Protection Against Counterfeit Products: </h3>
                    <p>A trademark helps consumers distinguish your products and services from others and protects you against counterfeit products2</p>
                    <h3>Exclusive Rights: </h3>
                    <p> A trademark gives you the exclusive right to use your mark and helps prevent competitors from using a mark that’s similar to yours3.</p>
